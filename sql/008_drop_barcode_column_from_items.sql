-- items からバーコード(JANコード)列を削除する
-- KURA:Sからバーコードスキャン機能を廃止したことに伴う変更
--
-- 【注意】このSQLは items.barcode に保存されている既存のバーコード値を
-- 完全に削除します。実行後は元に戻せません(バックアップが必要な場合は
-- 事前に別途取得してください)。
--
-- barcode列に紐づくインデックス・制約(存在する場合)は、列の削除に伴って
-- 通常は自動的に削除されます。他のビュー等がこの列に依存している場合は
-- このSQLがエラーになるため、その場合は個別に確認してください。

begin;

alter table items drop column if exists barcode;

commit;
