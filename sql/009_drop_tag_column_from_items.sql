-- items からタグ列を削除する
-- KURA:Sのタグ機能を廃止し、カテゴリー(product_master.category、無ければitems.category)・
-- サブカテゴリー(product_master.sub_category)による分類へ統一したことに伴う変更
--
-- 【注意】このSQLは items.tag に保存されている既存のタグ値を完全に削除します。
-- 実行後は元に戻せません。商品マスタが未作成の商品は、タグが持っていた分類情報の
-- 代わりとなる列が items 側に無いため、削除後は分類情報が失われます
-- (商品マスタを作成すれば、そちらのカテゴリー/サブカテゴリーで分類されます)。
--
-- tag列に紐づくインデックス・制約(存在する場合)は、列の削除に伴って
-- 通常は自動的に削除されます。他のビュー等がこの列に依存している場合は
-- このSQLがエラーになるため、その場合は個別に確認してください。

begin;

alter table items drop column if exists tag;

commit;
